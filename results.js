function reviewPrompt(session) {
  const reviewDate = new Date()
  reviewDate.setDate(reviewDate.getDate() + 7)
  return `请在 ${formatDate(reviewDate)} 重新检验这次判断：\n\n问题：${session.question}\n当前判断：${session.final}\n原定行动：${session.action}\n\n复查时回答：\n1. 现实中实际发生了什么？\n2. 哪个前提被证实或推翻？\n3. 我是否按照原定行动执行？\n4. 现在的判断和确信程度是什么？`
}

function selectedShareOptions(container) {
  return {
    question: Boolean(container.querySelector('[data-share-field="question"]')?.checked),
    judgment: Boolean(container.querySelector('[data-share-field="judgment"]')?.checked),
    action: Boolean(container.querySelector('[data-share-field="action"]')?.checked),
  }
}

function bindResultTools(container, session) {
  const refreshPreview = () => {
    const preview = container.querySelector('[data-share-preview]')
    if (preview) preview.textContent = privacyShareText(session, selectedShareOptions(container))
  }

  container.querySelectorAll('[data-share-field]').forEach((input) => {
    input.addEventListener('change', refreshPreview)
  })

  container.addEventListener('click', async (event) => {
    const action = event.target.closest('[data-result-action]')?.dataset.resultAction
    if (!action) return

    const date = new Date(session.completedAt || session.updatedAt).toISOString().slice(0, 10)
    const base = `socratic-kernel-judgment-${date}`

    if (action === 'markdown') download(`${base}.md`, markdownFor(session), 'text/markdown;charset=utf-8')
    if (action === 'text') download(`${base}.txt`, plainTextFor(session), 'text/plain;charset=utf-8')
    if (action === 'json') download(`${base}.json`, privateJsonFor(session), 'application/json;charset=utf-8')
    if (action === 'review') {
      const copied = await copyText(reviewPrompt(session))
      notify(copied ? '七天复查提示已复制。' : '复制失败，请手动保存。')
    }
    if (action === 'copy-share') {
      const copied = await copyText(privacyShareText(session, selectedShareOptions(container)))
      notify(copied ? '隐私分享摘要已复制。' : '复制失败，请手动选择预览文字。')
    }
  })
}

function enhanceResult() {
  const recap = document.querySelector('#app .recap')
  if (!recap) return
  const questionArticle = recap.closest('.question')
  if (!questionArticle || questionArticle.hasAttribute(RESULT_ENHANCED_ATTR)) return

  const session = currentCompletedSession()
  if (!session) return

  questionArticle.setAttribute(RESULT_ENHANCED_ATTR, 'true')
  const inquiry = questionArticle.closest('.inquiry')
  inquiry?.insertAdjacentHTML('afterend', renderArtifact(session) + renderResultTools(session))
  const tools = document.querySelector('#app .result-tools')
  if (tools) bindResultTools(tools, session)
}

new MutationObserver(() => queueMicrotask(enhanceResult)).observe(document.querySelector('#app'), {
  childList: true,
  subtree: true,
})

enhanceResult()
