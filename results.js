const ARCHIVE_KEY = 'socratic-kernel:data:v2'
const LEGACY_ARCHIVE_KEY = 'kernel:socratic:v1'
const RESULT_ENHANCED_ATTR = 'data-result-enhanced'

function readArchive() {
  try {
    const raw = localStorage.getItem(ARCHIVE_KEY) || localStorage.getItem(LEGACY_ARCHIVE_KEY)
    const parsed = JSON.parse(raw || '{"sessions":[]}')
    return Array.isArray(parsed.sessions) ? parsed.sessions : []
  } catch {
    return []
  }
}

function escapeHtml(value = '') {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;')
}

function formatDate(value) {
  return new Intl.DateTimeFormat('zh-CN', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  }).format(new Date(value))
}

function currentCompletedSession() {
  const title = document.querySelector('#app .question h2')?.textContent?.trim()
  if (!title) return null
  return readArchive()
    .filter((session) => session.status === 'completed' && session.question === title)
    .sort((a, b) => new Date(b.updatedAt) - new Date(a.updatedAt))[0]
}

function strongestOpposition(session) {
  return (
    session.questions?.find(
      (question) => question.dimension === 'opposition' && String(question.answer || '').trim(),
    )?.answer || '尚未形成明确的最强反方摘要。'
  )
}

function dimensionCount(session) {
  return new Set((session.questions || []).map((question) => question.dimension)).size
}

function confidenceDelta(session) {
  return Number(session.confidenceAfter || 0) - Number(session.confidence || 0)
}

function confidenceLabel(session) {
  const delta = confidenceDelta(session)
  if (delta === 0) return '确信程度未变化'
  return delta > 0 ? `确信提高 ${delta} 个百分点` : `确信降低 ${Math.abs(delta)} 个百分点`
}

function markdownFor(session) {
  const answers = (session.questions || [])
    .map(
      (question, index) =>
        `### ${String(index + 1).padStart(2, '0')} · ${question.dimension || 'inquiry'}\n\n**问题：** ${question.text}\n\n**回答：** ${question.answer || '未回答'}`,
    )
    .join('\n\n')

  return `# Socratic Kernel 判断档案\n\n> 这是用户在当前证据下亲自确认的暂时判断，不是系统替用户生成的最终答案。\n\n- 完成时间：${formatDate(session.completedAt || session.updatedAt)}\n- 模式：${session.mode || 'reflection'}\n- 初始确信：${session.confidence}%\n- 审议后确信：${session.confidenceAfter}%\n- 变化：${confidenceLabel(session)}\n\n## 审议问题\n\n${session.question}\n\n## 初始立场\n\n${session.position}\n\n## 初始依据\n\n${session.evidence || '未填写'}\n\n## 结构化追问记录\n\n${answers}\n\n## 最强反方\n\n${strongestOpposition(session)}\n\n## 当前判断\n\n${session.final}\n\n## 下一步行动\n\n${session.action}\n\n## 愿意承担的责任\n\n${session.responsibility || '尚未写下'}\n\n---\n\nGenerated by Socratic Kernel · https://github.com/SiruGao/socratic-kernel\n`
}

function plainTextFor(session) {
  return markdownFor(session)
    .replace(/^#{1,6}\s+/gm, '')
    .replace(/^>\s?/gm, '')
    .replace(/\*\*/g, '')
    .replace(/---/g, '')
    .trim()
}

function privateJsonFor(session) {
  return JSON.stringify(
    {
      schema: 'socratic-kernel-judgment/v1',
      exportedAt: new Date().toISOString(),
      session,
    },
    null,
    2,
  )
}

function privacyShareText(session, options = {}) {
  const lines = [
    '我刚完成了一次 Socratic Kernel 审议。',
    `经过 ${session.questions?.length || 0} 个问题、${dimensionCount(session)} 个思考维度，${confidenceLabel(session)}。`,
  ]

  if (options.question) lines.push(`审议问题：${session.question}`)
  if (options.judgment) lines.push(`当前判断：${session.final}`)
  if (options.action) lines.push(`下一步行动：${session.action}`)

  lines.push('它没有替我做决定，只帮助我检验前提、证据、反方与责任。')
  lines.push('https://github.com/SiruGao/socratic-kernel')
  return lines.join('\n')
}

function download(filename, content, type) {
  const url = URL.createObjectURL(new Blob([content], { type }))
  const link = document.createElement('a')
  link.href = url
  link.download = filename
  link.click()
  setTimeout(() => URL.revokeObjectURL(url), 0)
}

async function copyText(text) {
  try {
    await navigator.clipboard.writeText(text)
    return true
  } catch {
    const textarea = document.createElement('textarea')
    textarea.value = text
    textarea.style.position = 'fixed'
    textarea.style.opacity = '0'
    document.body.append(textarea)
    textarea.select()
    const copied = document.execCommand('copy')
    textarea.remove()
    return copied
  }
}

function notify(message) {
  let element = document.querySelector('.result-notice')
  if (!element) {
    element = document.createElement('div')
    element.className = 'result-notice'
    document.body.append(element)
  }
  element.textContent = message
  element.classList.add('show')
  clearTimeout(element._timer)
  element._timer = setTimeout(() => element.classList.remove('show'), 2600)
}

function renderArtifact(session) {
  const delta = confidenceDelta(session)
  const deltaSymbol = delta === 0 ? '±0' : delta > 0 ? `+${delta}` : String(delta)
  const date = formatDate(session.completedAt || session.updatedAt)

  return `<section class="judgment-artifact" aria-labelledby="judgment-artifact-title">
    <div class="artifact-folio"><span>SOCRATIC KERNEL / JUDGMENT</span><span>${escapeHtml(date)}</span></div>
    <div class="artifact-main">
      <div class="artifact-index" aria-hidden="true">J—${String((session.questions || []).length).padStart(2, '0')}</div>
      <div class="artifact-copy">
        <p class="eyebrow">A judgment you chose to own</p>
        <h2 id="judgment-artifact-title">${escapeHtml(session.final)}</h2>
        <p class="artifact-question">关于：${escapeHtml(session.question)}</p>
      </div>
      <div class="artifact-confidence">
        <small>CONFIDENCE SHIFT</small>
        <strong>${escapeHtml(deltaSymbol)}</strong>
        <span>${session.confidence}% → ${session.confidenceAfter}%</span>
      </div>
    </div>
    <div class="artifact-grid">
      <div><small>最强反方</small><p>${escapeHtml(strongestOpposition(session))}</p></div>
      <div><small>下一步行动</small><p>${escapeHtml(session.action)}</p></div>
      <div><small>愿意承担</small><p>${escapeHtml(session.responsibility || '尚未写下')}</p></div>
      <div><small>审议结构</small><p>${session.questions?.length || 0} 个问题 · ${dimensionCount(session)} 个维度</p></div>
    </div>
  </section>`
}

